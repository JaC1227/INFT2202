// export will export the functions so they can be imported elsewhere

/**
 * Calculates the half of a number
 * @param {*} x
 * @returns
 */
export function halfOf(x)
{
    return x / 2;
}

/**
 * Mulitply 2 numbers
 * @param {*} x, @param {*} y 
 * @returns
 */
export function mulitply(x, y)
{
    return x * y;
}